const Swal = require('sweetalert2');

// (navigate) 버튼 2개 가진 경고창 함수나 이벤트를 인자로 받음
export const failedNavigateAlert = (
  title: string,
  text: string,
  where: string,
  navigate: any
) => {
  Swal.fire({
    title: title,
    text: text,
    icon: 'warning',
    confirmButtonText: 'Login',
    confirmButtonColor: '#e35c02',
    focusDeny: true,
    showDenyButton: true,
    denyButtonText: 'Cancel',
    denyButtonColor: '#707070',
    showCancelButton: false,
  }).then((result: any) => {
    result.isConfirmed && navigate(where);
  });
};

// (axios) 버튼 2개 가진 경고창 함수나 이벤트를 인자로 받음
export const failedAxiosAlert = (
  title: string,
  text: string,
  axiosFunction: () => void
) => {
  Swal.fire({
    title: title,
    text: text,
    icon: 'warning',
    confirmButtonText: 'Delete',
    confirmButtonColor: '#e35c02',
    focusDeny: true,
    showDenyButton: true,
    denyButtonText: 'Cancel',
    denyButtonColor: '#707070',
    showCancelButton: false,
  }).then((result: any) => {
    result.isConfirmed && axiosFunction();
  });
};

// 일반 경고창 OK 버튼 한 개
export const warningAlert = (title: string, text: string) => {
  Swal.fire({
    title: title,
    text: text,
    icon: 'warning',
    confirmButtonText: 'OK',
    confirmButtonColor: '#e35c02',
    showCancelButton: false,
  });
};

export const successAlert = (title: string, text: string) => {
  Swal.fire({
    position: 'center',
    icon: 'success',
    title: title,
    text: text,
    showConfirmButton: true,
    confirmButtonText: 'OK',
    confirmButtonColor: '#e35c02',
  });
};

// 확인 알림창
export const infoAlert = (title: string, text: string) => {
  Swal.fire(title, text, 'info');
};

export const autoCloseAlert = (icon: string, text: string) => {
  Swal.fire({
    position: 'center',
    icon: icon,
    text: text,
    padding: '3rem',
    showConfirmButton: false,
    timer: 1500,
  });
};
